                <h2>Onderwijs</h2>
                <p>
                    Unie voor Vooruitgang is een politieke partij die zich richt op het aanpakken van de ongelijkheid tussen het Middelbaar Beroepsonderwijs (MBO) en het Hoger Beroepsonderwijs (HBO) in het huidige schoolsysteem. Ze erkennen dat het huidige denken over MBO-opleidingen als minderwaardig ten opzichte van het HBO leidt tot een onbalans in de maatschappij en een tekort aan MBO-opgeleide mensen. Unie voor Vooruitgang ziet dit gat als cruciaal en wil maatregelen nemen om de waardering van MBO-opleidingen te vergroten.

                    Een andere oorzaak van het tekort aan MBO-opgeleide mensen wordt volgens unie voor Vooruitgang veroorzaakt door het salarisverschil tussen MBO- en HBO-/universitair opgeleide personen, evenals de baanzekerheid. Ze willen stappen ondernemen om de inkomenskloof te verkleinen en meer mogelijkheden voor carrièregroei en baanzekerheid te bieden aan MBO-afgestudeerden.
                </p>
                <p>
                    De Unie voor Vooruitgang is een politieke partij die zich bezighoudt met het onderwijs. Daarom willen wij de doorstroming van het middelbaar beroepsonderwijs (MBO) naar het hoger beroepsonderwijs (HBO) bevorderen. We stellen daarom voor om elke HBO-school te verplichten een streeftraject voor de instroom van MBO naar HBO uitdagender te maken, zodat studenten meer inspanning moeten leveren. Dit draagt ook bij aan de taalvaardigheid van HBO-studenten.
                </p>
