                    Met het coalitieakkoord investeerde het huidige kabinet reeds structureel € 3 miljard in Defensie (waarvan € 1,9 miljard in 2023). In reactie op de oorlog in Oekraïne voegde het kabinet daar afgelopen voorjaar structureel € 2 miljard aan toe. Deze extra investeringen waren bedoeld om de operationele gereedheid, gevechtskracht en wendbaarheid van de krijgsmacht te verbeteren, evenals om innovatie en samenwerking met partners in de EU en NAVO te bevorderen.Door te bezuinigen op Defensie, zou het mogelijk zijn om een eerlijk salaris te waarborgen voor MBO-geschoolde medewerkers, zodat zij een goed salaris krijgen volgens de geldende CAO. Salarisverhoging: We zullen streven naar een rechtvaardige salarisverhoging voor MBO-gediplomeerden, zodat hun loon beter in lijn komt met andere onderwijsniveaus. Dit kan bereikt worden door middel van onderhandelingen met werkgevers, het invoeren van wettelijke minimumsalarissen en het stimuleren van collectieve arbeidsovereenkomsten (CAO's) die eerlijke salarissen waarborgen.
                </p>
                <p>
                    Een belangrijk aspect van het promoten van MBO-opleidingen is het verstrekken van gedetailleerde voorlichting over de inhoud van de opleidingen. Dit kan onder andere worden gedaan door voorlichtingsbijeenkomsten, open dagen, en online informatiebronnen aan te bieden. Tijdens deze voorlichtingsactiviteiten kunnen potentiële studenten inzicht krijgen in de verschillende vakken, vaardigheden en praktische trainingen die deel uitmaken van een MBO-opleiding. Dit helpt hen om een beter voorbeeld te hebben van wat ze kunnen verwachten en welke kennis en vaardigheden ze kunnen ontwikkelen tijdens de opleiding.
                </p>
