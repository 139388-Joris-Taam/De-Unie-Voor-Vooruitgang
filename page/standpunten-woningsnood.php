        <h2>Woningsnood</h2>
        De Unie voor Vooruitgang (UVV) maakt zich ernstige zorgen over de hoge huurprijzen en woonprijzen in ons land. Steeds meer burgers hebben moeite om de huur op te brengen, met name burgers met een middeninkomen. Dit komt doordat de huurprijzen zijn gestegen en er steeds minder betaalbare huizen beschikbaar zijn.<br>
        <br>
        De grootste oorzaak van de stijgende huurprijzen is de toename van privé verhuurders. Deze mensen verhuren hun eigen woning aan andere, vaak voor een hogere prijs dan een woningcorporatie zou doen. Hierdoor is er meer concurrentie op de huurmarkt en zijn de prijzen flink gestegen.<br>
        <br>
        Een andere belangrijke oorzaak is het gebrek aan nieuwe bouwprojecten tussen 2010 en 2015. Er is destijds te weinig gebouwd, waardoor er nu een tekort is aan betaalbare huizen. Dit probleem wordt nog vervolgd door het stikstof probleem en door de aanwezigheid van PFAS (Poly- en perfluoralkystoffen) in de grond, waardoor het lastiger is geworden om bouwprojecten te realiseren.<br>
        <br>
        Daarnaast is er sprake van een toename van alleenstaande en een gemiddelde leefruimte van 10 vierkante meter per persoon. Hierdoor is er meer vraag naar kleinere huizen, terwijl het aanbod van betaalbare, kleine huizen beperkt is. Ook spelen vastgoed investeerders een rol, Hun opkopen woningen en verhuren of verkopen deze vervolgens voor een duurdere prijs.<br>
        <br>
        Om deze problemen aan te pakken heeft de UVV verschillende maatregelen voorgesteld.<br>
        Zo zouden diverse gemeentes huizen kunnen bouwen op boerenlanden indien deze om worden gekocht. Ook zouden leegstaande gebouwen zoals een kantoor of een oude school kunnen worden omgebouwd tot appartementen. Ook zou de gemeenten bestaande kantoren kunnen ombouwen indien het bedrijf in dat kantoor wilt verhuizen.<br>
        Ook dacht de UVV aan een limiet geven aan het maximaal aantal huizen dat een persoon mag verhuren. Wij dachten te denken aan maximaal twee huurwoningen per persoon.<br>
        Daarnaast wouden we ook nog belasting op huursubsidies doen. Waardoor telkens aan de maandelijkse huur word opgehaald door de verhuurder dat hij of zij nu 21% aan belasting betalen.<br>
        <br>
        Het is belangrijk dat de overheid deze problemen serieus neemt en maatregelen neemt om de huurmarkt te reguleren. De huurprijzen zijn de afgelopen jaren fors gestegen en het word steeds moeilijker om een betaalbare woning te vinden. Dit heeft niet alleen gevolgen voor mensen met een laag inkomen, maar ook voor burgers met een middeninkomen die moeite hebben om de huur op te brengen.<br>
        <br>
        Het is daarom goed dat de UVV deze problemen aankaart en oplossingen aandraagt. Het is aan de overheid om deze oplossingen serieus te nemen en te zorgen voor een betaalbare huurmarkt voor iedereen. Het is immers een basisbehoefte van iedereen om een dak boven het hoofd te hebben en daar moet iedereen toegang tot hebben.<br>
